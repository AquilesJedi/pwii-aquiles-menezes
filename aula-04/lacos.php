<?php

    //WHILE
    echo "While <br>";
    $contador = 1;

    while ($contador < 6) {
        echo $contador, "<br>";
        $contador++;
    }

    //DO WHILE  
    echo "Do While <br>";

    $contadorii = 1;

    do {
        echo $contadorii , "<br>";
        $contadorii++;
    } while ($contadorii < 6);

    //FOR
    echo "<br> For <br>";

    for ($contadoriii = 0; $contadoriii <= 10; $contadoriii++) {
        echo $contadoriii, "<br>";
    }

?>